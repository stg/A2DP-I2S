// Perform audio processing
// * input detection
// * synthesizing
// * routing
// * mixing
static void audio_processor(void *dummy) {

  audio_event e = EVT_READY;
  xQueueSend(eventQueue, &e, portMAX_DELAY);

  while(1) {

    if(a2dp_sink.is_connected()) {
      if(connected_counter == 0) {
        audio_event e = EVT_BT_CONNECT;
        xQueueSend(eventQueue, &e, portMAX_DELAY);
      }
      if(connected_counter < 4000) {
        if(++connected_counter == 4000) {
          a2dp_sink.set_discoverability(ESP_BT_GENERAL_DISCOVERABLE);
        }
      }
    } else {
      if(connected_counter) {
        audio_event e = EVT_BT_DISCONNECT;
        xQueueSend(eventQueue, &e, portMAX_DELAY);
      }
      connected_counter = 0;
    }

    // Check A2DP buffer, switch streaming on/off by hysteresis
    size_t a2dp_available = read_a2dp_chunk(NULL);
    if(streaming) {
      if(a2dp_available < AUDIO_CHUNK_SIZE) {
        streaming = false;
        streaming_stable = STABILITY_COUNT_OUT;
        cdt_reset();
        audio_event e = EVT_BT_STOP;
        xQueueSend(eventQueue, &e, portMAX_DELAY);
      }
    } else {
      if(a2dp_available >= BUFFER_NOMINAL) {
        streaming = true;
        streaming_stable = STABILITY_COUNT_IN;
        audio_event e = EVT_BT_START;
        xQueueSend(eventQueue, &e, portMAX_DELAY);
      }
    }

    // If source is not I2S, read it first to maintain timing
    // Actual source will overwrite the chunk later
    if(source != SRC_ANALOG) read_i2s_chunk(chunk);

    if(streaming) {

      // If streaming from the A2DP buffer, process it now
      // We need to prevent overflows and track clock drift even when it's not the source
      // Actual source will overwrite the chunk later
      read_a2dp_chunk(chunk);

    } else if(source == SRC_BT) {

      // Mute the chunk when A2DP is the source but is not streaming properly
      memset(chunk, 0, sizeof(chunk));
    }

    // If source is I2S, read it last
    if(source == SRC_ANALOG) read_i2s_chunk(chunk);

    // Callback for input audio data
    if(cb_audiodata) cb_audiodata((int16_t *)chunk, true);

    // Switch sources after streaming state appears stable
    if(streaming_stable) {
      if(!--streaming_stable) {
        audio_event e = EVT_BT_STABLE;
        xQueueSend(eventQueue, &e, portMAX_DELAY);
      }
    }

    // Instant commanded fades when buffer fader is fully engaged
    if(fader_buffer == 0 && fader != fader_commanded) {
      fader = fader_commanded;
    }

    // Handle synthesis request
    if(synthesis_request) {

      synthesis = true;
      synthesis_request = false;
      if(synthesis_mp3_size) {
        if(mp3_decoder) MP3FreeDecoder(mp3_decoder);
        mp3_decoder = MP3InitDecoder();
        mp3_buffer_index = mp3_buffer_fill = 0;
        mp3_data = (typeof(mp3_data))synthesis_mp3_data;
        mp3_size = (typeof(mp3_size))synthesis_mp3_size;
        synthesis_mp3_size = 0;
      } else if(synthesis_sweep_seed.t) {
        synthesis_sweep_data = synthesis_sweep_seed;
        synthesis_sweep_seed.t = 0;
      } else {
        synthesis = false;
      }

    }

    // Process samples
    int16_t *ch = (int16_t *)chunk;
    for(int32_t n = 0; n < SAMPLES(AUDIO_CHUNK_SIZE); n++, ch += 2) {

      // Process application fader
      if(fader < fader_commanded) {
        fader++;
      } else if(fader > fader_commanded) {
        fader--;
      }

      // Fading complete event
      if(fader == fader_commanded && fade_request) {
        fade_request = false;
        audio_event e = EVT_FADE_DONE;
        xQueueSend(eventQueue, &e, portMAX_DELAY);
      }

      // Apply fader using a cosine profile to eliminate the zipper effect
      uint32_t fade = (cosine((fader << 17) + 0x80000000) >> 17) + 8192;
      ch[0] = ((int32_t)ch[0] * fade) >> 14;
      ch[1] = ((int32_t)ch[1] * fade) >> 14;

      if(synthesis) {
        bool synth_done = false;
        if(mp3_size || mp3_buffer_index < mp3_buffer_fill) {

          // Decode MP3
          if(mp3_buffer_index >= mp3_buffer_fill) {
            int offset = MP3FindSyncWord(mp3_data, mp3_size);
            if(offset < 0) {
              synth_done = true;
            } else {
              mp3_data += offset;
              mp3_size -= offset;
              if(MP3Decode(mp3_decoder, (unsigned char**)&mp3_data, &mp3_size, mp3_buffer, 0) == ERR_MP3_NONE) {
                MP3GetLastFrameInfo(mp3_decoder, &mp3_info);
                mp3_buffer_index = 0;
                mp3_buffer_fill = mp3_info.outputSamps;
              } else {
                synth_done = true;
              }
            }
          }
          if(synth_done) {
            if(mp3_decoder) MP3FreeDecoder(mp3_decoder);
            mp3_size = 0;
            mp3_buffer_index = 0;
            mp3_buffer_fill = 0;
          } else {
            if(mp3_info.nChans == 2) {
              ch[0] += mp3_buffer[mp3_buffer_index++] >> 2;
              ch[1] += mp3_buffer[mp3_buffer_index++] >> 2;
            } else {
              ch[0] += mp3_buffer[mp3_buffer_index  ] >> 2;
              ch[1] += mp3_buffer[mp3_buffer_index++] >> 2;
            }
          }

        } else if(synthesis_sweep_data.t) {
          
          // Approximate log-sweep by blazing fast fixed point
          synthesis_sweep_data.t--;
          uint32_t b32_mod = (uint64_t)((synthesis_sweep_data.b32v - (0x40000000 >> synthesis_sweep_data.b32q)) * (uint64_t)synthesis_sweep_data.f32 << synthesis_sweep_data.b32q >> 12);
          int32_t sample = sine(b32_mod);
          sample = (sample + (1 << 16)) >> 17;
          synthesis_sweep_data.b32v = smmlar(synthesis_sweep_data.b32v, synthesis_sweep_data.b32, synthesis_sweep_data.b32v);
          if(synthesis_sweep_data.b32v < 0) {
            synthesis_sweep_data.b32v = (uint32_t)synthesis_sweep_data.b32v >> 1;
            synthesis_sweep_data.b32q++;
          }
          ch[0] += sample;
          ch[1] += sample;

        } else {
          
          // TODO: Add other synthesis methods here
          synth_done = true;

        }
        if(synth_done) {
          synthesis = false;
          audio_event e = EVT_SYNTH_DONE;
          xQueueSend(eventQueue, &e, portMAX_DELAY);
        }
      }
    }

    // Callback for output audio data
    if(cb_audiodata) cb_audiodata((int16_t *)chunk, false);

    // Write audio to I2S
    write_i2s_chunk(chunk);
  }
}

// Initialize audio driver
void Audio_Driver::init(const char *name, audio_samplerate samplerate, bool preemption, bool mclk) {

  // Free some unused memory allocated to BLE (which we will never use)
  //esp_bt_mem_release(ESP_BT_MODE_BLE); // 4,728 freed => 178000 total on 3.0.7

  sink_samplerate = samplerate;

  buffer_access = xSemaphoreCreateBinary();
  xSemaphoreGive(buffer_access);

  // Start I²S
  if(mclk) {
    i2s_device.setPins(I2S_BCK, I2S_WCK, I2S_TXD, I2S_RXD, I2S_MCK);
    //gpio_set_drive_capability(I2S_MCK, GPIO_DRIVE_CAP_2);  // Changing this may vastly improve signal integrity
  } else {
    i2s_device.setPins(I2S_BCK, I2S_WCK, I2S_TXD, I2S_RXD);
  }
  if(!i2s_device.begin(I2S_MODE_STD, samplerate, I2S_DATA_BIT_WIDTH_16BIT, I2S_SLOT_MODE_STEREO, I2S_STD_SLOT_BOTH)) ESP_ERROR_CHECK(ESP_FAIL);

  // Start A2DP Bluetooth service
  a2dp_sink.set_avrc_metadata_attribute_mask(ESP_AVRC_MD_ATTR_TITLE | ESP_AVRC_MD_ATTR_ARTIST | ESP_AVRC_MD_ATTR_ALBUM | ESP_AVRC_MD_ATTR_PLAYING_TIME);
  a2dp_sink.set_avrc_metadata_callback(avrc_metadata_callback);
  a2dp_sink.set_avrc_connection_state_callback(avrc_connection_state_callback);
  //a2dp_sink.set_avrc_rn_playstatus_callback(avrc_rn_playstatus_callback);
  //a2dp_sink.set_avrc_rn_play_pos_callback(avrc_rn_play_pos_callback);
  
  // This delay prevents crashes due to excessive CPU use when A2DPSink wants to write to a full buffer
  a2dp_sink.set_max_write_delay_ms(1);
  
  a2dp_sink.start(name);
  
  // Allow incoming connections to disconnect current connection (take over)
  a2dp_sink.enable_preemption(preemption);

  // Configure source clock drift filter
  biquad_filter f = biquadMake(FILTER_LOWPASS, (float)samplerate / SAMPLES(AUDIO_CHUNK_SIZE), 1.0, 0.7);
  cdt_filter = biquadQ(&f, 30);

  // Fetch channels for lower-layer I2S access
  rx_chan = i2s_device.rxChan();
  tx_chan = i2s_device.txChan();

  // Create event queue
  eventQueue = xQueueCreate(EVENT_QUEUE_SIZE, sizeof(audio_event));

  // Create event handler task if a callback was registered before init.
  if(cb_eventhandler) {
    create_event_handler_task();
  }
  
}

// Start everything
void Audio_Driver::begin() {
  // Create application
  xTaskCreate(audio_processor, "Audio Processor", 1024, NULL, 1, NULL);
}

// Play MP3
void Audio_Driver::startSynthMP3(res_id mp3) {
  if(synthesis_request) return;
  const unsigned char *data = resourceData(mp3);
  size_t size = resourceSize(mp3);
  if(!data || size == 0) return;
  synthesis_mp3_data = data;
  synthesis_mp3_size = size;
  synthesis_request = true;
}

// Play log-sweep
void Audio_Driver::startSynthSweep(sweep_data seed) {
  if(synthesis_request) return;
  synthesis_sweep_seed = seed;
  synthesis_request = true;
}

