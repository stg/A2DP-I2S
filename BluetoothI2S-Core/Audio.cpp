// Send an AVRC command
void Audio_Driver::sendCommand(audio_command command) {
  switch(command) {
    case CMD_PLAY:   a2dp_sink.play();         break;
    case CMD_PAUSE:  a2dp_sink.pause();        break;
    case CMD_STOP:   a2dp_sink.stop();         break;
    case CMD_NEXT:   a2dp_sink.next();         break;
    case CMD_PREV:   a2dp_sink.previous();     break;
    case CMD_FWD:    a2dp_sink.fast_forward(); break;
    case CMD_REW:    a2dp_sink.rewind();       break;
    case CMD_VOLINC: a2dp_sink.volume_up();    break;
    case CMD_VOLDEC: a2dp_sink.volume_down();  break;
    default: break;
  }
}

void Audio_Driver::sendCommand(audio_command command, bool pressed) {
  esp_avrc_pt_cmd_t avrc_cmd;
  switch(command) {
    case CMD_PLAY:   avrc_cmd = ESP_AVRC_PT_CMD_PLAY;         break;
    case CMD_PAUSE:  avrc_cmd = ESP_AVRC_PT_CMD_PAUSE;        break;
    case CMD_STOP:   avrc_cmd = ESP_AVRC_PT_CMD_STOP;         break;
    case CMD_NEXT:   avrc_cmd = ESP_AVRC_PT_CMD_FORWARD;      break;
    case CMD_PREV:   avrc_cmd = ESP_AVRC_PT_CMD_BACKWARD;     break;
    case CMD_FWD:    avrc_cmd = ESP_AVRC_PT_CMD_FAST_FORWARD; break;
    case CMD_REW:    avrc_cmd = ESP_AVRC_PT_CMD_REWIND;       break;
    case CMD_VOLINC: avrc_cmd = ESP_AVRC_PT_CMD_VOL_UP;       break;
    case CMD_VOLDEC: avrc_cmd = ESP_AVRC_PT_CMD_VOL_DOWN;     break;
    default: return;
  }
  esp_avrc_ct_send_passthrough_cmd(0, avrc_cmd, pressed ? ESP_AVRC_PT_CMD_STATE_PRESSED : ESP_AVRC_PT_CMD_STATE_RELEASED);
}

// Set audio event handler
  void Audio_Driver::setEventHandler(void (*cb)(audio_event)) {
    cb_eventhandler = cb;
    if(!eventQueue) return;
    if(eventHandler) {
      if(!cb) {
      	// Delete event handler
        vTaskDelete(eventHandler);
        eventHandler = NULL;
      }
    } else {
      if(cb) {
      	// Create event handler
        create_event_handler_task();
      }
    }
  }

// Disconnect current user
void Audio_Driver::disconnectBluetooth() {
  a2dp_sink.disconnect();
}
